def seno(n1): #Esta función debe solicitar ángulo en grados para luego
#convertir a radianes y así calculará el seno.
    radianes_ang = math.radians(n1)
    return round (math.sin(radianes_ang), 2)

def tangente(n1): #Esta función debe solicitar ángulo en grados para luego
#convertir a radianes y así calculará la tangente.
    radianes_ang = math.radians(n1)
    return round (math.tan(radianes_ang), 2)

