def suma (a,b):
    return a + b

def resta (a,b):
    return a - b

def multiplicacion (a,b):
    return a * b

def division (a,b):
    if b == 0: #No es posible dividir por cero, entonces:
      return "No es posible hacer la operación"
    return round ((a / b), 2) #Agregamos round para ajustar cantidad de decimales

