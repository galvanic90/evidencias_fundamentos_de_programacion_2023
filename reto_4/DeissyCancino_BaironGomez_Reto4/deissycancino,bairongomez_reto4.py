#OPERACIONES BÁSICAS Y ARITMÉTICAS EXTENDIDAS
def suma(n1, n2):
    return n1 + n2

def resta(n1, n2):
    return n1 - n2

def multiplicacion(n1, n2):
    return n1 * n2

def division(n1, n2):
    if n2 == 0:
        return "No se puede divir por 0"
    return n1 / n2

def division_entera(n1, n2):
    if n2 == 0:
        return "No se puede divir por 0"
    return n1 // n2

def residuo(n1, n2):
    if n2 == 0:
        return "No se puede divir por 0"
    return n1 % n2

def potencia(n1, n):
    return n1 ** n

def raiz_n(n1, n):
    if n == 0:
        return "No se puede dividir por 0"
    return n1 ** (1/n)


def valor_absoluto(n1):
   return abs(n1)


def inverso(n1):
    if n1 == 0:
        return "No se puede dividir por 0"
    return 1 / n1

def factorial(n1):
    if n1 < 0:
        return "No se puede realizar el factorial de un número negativo"
    elif n1 == 0:
        return 1
    else:
        return n1 * factorial(n1 - 1)

