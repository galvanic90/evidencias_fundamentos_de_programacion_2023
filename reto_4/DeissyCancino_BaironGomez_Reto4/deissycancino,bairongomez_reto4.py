def seno(angulo_grados):
    # Convertir el ángulo de grados a radianes
    angulo_rad = angulo_grados * (3.14159265358979323846 / 180.0)
    # Calcular el seno utilizando la serie de Taylor
    seno_aprox = angulo_rad
    for n in range(1, 10):
        termino = ((-1) ** n) * (angulo_rad ** (2 * n + 1)) / factorial(2 * n + 1)
        seno_aprox += termino
    return seno_aprox

