def promedio(datos):
    return sum(datos) / len(datos)


def mediana(datos):
    datos.sort()
    n = len(datos)
    if n % 2 == 0:
      mediana = (datos[n // 2 - 1] + datos[n // 2]) / 2
    else:
      mediana = datos[n // 2]
    return mediana

def moda(datos):
    conteo = {}
    for dato in datos:
      if dato in conteo:
        conteo[dato] += 1
      else:
        conteo[dato] = 1
    moda = [key for key, value in conteo.items() if value == max(conteo.values())]
    if len(moda) == len(datos):
      print("No hay una moda clara en los datos.")
    else:
      print(f"La moda es: {', '.join(map(str, moda))}")



